    public void renderDataFormShow(String id){
        setTitle("Thông tin khóa học");  
        CourseDTO x = courseBUS.GetCourseById(Integer.parseInt(id));
        txt_courseID.setText(x.getCourseID()+"");
        txt_title.setText(x.getTitle());
        txt_Credit.setText(x.getCredits());
        loadDepartmentid(x.getDepartmentID()+"");  
        if(onlineCourseBUS.GetOnlineCourseById(Integer.parseInt(id))!=null){
            jcb_Online.setSelected(true);
            jcb_Online.setEnabled(false);
            OnlineCourseDTO y = onlineCourseBUS.GetOnlineCourseById(Integer.parseInt(id));
            txt_url.setText(y.getUrl());
        }else{
            txt_url.setEditable(false);
        }  
        if(onsiteCourseBUS.GetOnsiteCourseById(Integer.parseInt(id))!=null){
            jcb_onsite.setSelected(true);
            jcb_onsite.setEnabled(false);
            OnsiteCourseDTO z = onsiteCourseBUS.GetOnsiteCourseById(Integer.parseInt(id));
            txt_Location.setText(z.getLocation());
            txt_Days.setText(z.getDays());
            txt_Time.setText(z.getCourse_time());
        }else{
            
            txt_Location.setEditable(false);
            txt_Time.setEditable(false);
            txt_Days.setEditable(false);
        }
        
        
    }
    public void renderDataFormAdd(){
        setTitle("Thêm khóa học");  
        ArrayList<DepartmentDTO> listDPid= departmentBUS.getList(); 
        for(DepartmentDTO x : listDPid){
            jcb_deparmentID.addItem(x.getDepartmentID()+"");
        }
        txt_courseID.setText(courseBUS.getList().size()+1+"");
        
    }
    public void loadDepartmentid(String key){
        ArrayList<DepartmentDTO> listDPid= departmentBUS.getList(); 
        for(DepartmentDTO x : listDPid){
            jcb_deparmentID.addItem(x.getDepartmentID()+"");
        }
        for(int i=0;i<listDPid.size();i++){
            if(key.equals(jcb_deparmentID.getItemAt(i))) jcb_deparmentID.setSelectedIndex(i);
        }
        
    }
