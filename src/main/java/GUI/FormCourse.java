    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        CourseDTO x;
        OnlineCourseDTO online = null;
        OnsiteCourseDTO onsite =null;
        boolean ck1=false;
        boolean ckonsite=false; 
        boolean ckonline=false;
        if(func.equals("add")){
            x = new CourseDTO();
            x.setCourseID(Integer.parseInt(txt_courseID.getText()));
            x.setCredits(txt_Credit.getText());
            x.setDepartmentID(Integer.parseInt(jcb_deparmentID.getSelectedItem()+""));
            x.setTitle(txt_title.getText());
            if(courseBUS.validateData(x)){
                 ck1=true;
            }else JOptionPane.showMessageDialog(this, "cannot be empty.\nPlease enter valid.", "Notificatioin", 2);
    
            if(jcb_Online.isSelected()&&ck1==true){
                online = new OnlineCourseDTO();
                online.setCourseID(x.getCourseID());
                online.setUrl(txt_url.getText());
                ckonline=true;
                if(onlineCourseBUS.validateData(online)){
                    ck1=true;    
                }else ck1=false;
            }
            if(jcb_onsite.isSelected()&&ck1==true){
                onsite = new OnsiteCourseDTO();
                onsite.setCourseID(x.getCourseID());
                onsite.setLocation(txt_Location.getText());
                onsite.setCourse_time(txt_Time.getText());
                onsite.setDays(txt_Days.getText());
                ckonsite=true;
                try {
                    if(onsiteCourseBUS.validateData(onsite)==true){
                        ck1=true;
                    }else ck1=false;      
                } catch (ParseException ex) {
                    Logger.getLogger(FormCourse.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if(ck1==false){
                JOptionPane.showMessageDialog(this, "Add Course Fail - Check Valid", "Notificatioin", 2);
            }
            else{
                courseBUS.AddCourse(x);
                if(ckonline==true) onlineCourseBUS.AddOnlineCourse(online);
                if(ckonsite==true) onsiteCourseBUS.AddOnsiteCourse(onsite);
                JOptionPane.showMessageDialog(this, "Add Course Successful", "Notificatioin", 2);
                MainForm.loadDataCourse2(table);
                dispose();
            }
                    

        }
        else{
            x = new CourseDTO();
            x.setCourseID(Integer.parseInt(txt_courseID.getText()));
            x.setCredits(txt_Credit.getText());
            x.setDepartmentID(Integer.parseInt(jcb_deparmentID.getSelectedItem()+""));
            x.setTitle(txt_title.getText());
            if(courseBUS.validateData(x)){
                 ck1=true;
            }else JOptionPane.showMessageDialog(this, "cannot be empty.\nPlease enter valid.", "Notificatioin", 2);
            if(jcb_Online.isSelected()&&ck1==true){
                online = new OnlineCourseDTO();
                online.setCourseID(x.getCourseID());
                online.setUrl(txt_url.getText());
                ckonline=true;
                if(onlineCourseBUS.validateData(online)){
                    ck1=true;    
                }else ck1=false;
            }
            if(jcb_onsite.isSelected()&&ck1==true){
                onsite = new OnsiteCourseDTO();
                onsite.setCourseID(x.getCourseID());
                onsite.setLocation(txt_Location.getText());
                onsite.setCourse_time(txt_Time.getText());
                onsite.setDays(txt_Days.getText());
                ckonsite=true;
                try {
                    if(onsiteCourseBUS.validateData(onsite)==true){
                        ck1=true;
                    }else ck1=false;      
                } catch (ParseException ex) {
                    Logger.getLogger(FormCourse.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if(ck1==false){
                JOptionPane.showMessageDialog(this, "Edit Course Fail - Check Valid", "Notificatioin", 2);
            }
            else{
                courseBUS.UpdateCourse(x);
                if(ckonsite==true){ 
                    if(onsiteCourseBUS.GetOnsiteCourseById(x.getCourseID())!=null)
                        onsiteCourseBUS.UpdateOnsiteCourse(onsite);
                    else 
                        onsiteCourseBUS.AddOnsiteCourse(onsite);
                }
                if(ckonline==true){ 
                    if(onlineCourseBUS.GetOnlineCourseById(x.getCourseID())!=null)
                        onlineCourseBUS.UpdateOnlineCourse(online);
                    else 
                        onlineCourseBUS.AddOnlineCourse(online);
                }
                JOptionPane.showMessageDialog(this, "Edit Course Successful", "Notificatioin", 2);
                    MainForm.loadDataCourse2(table);
                    dispose();
                
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

